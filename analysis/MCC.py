    @staticmethod
    def ellipse_center_from_coefficient(coefficient):
        A, B, C, D, E, F = map(float, coefficient)
        M = np.array([[2 * A, B], [B, 2 * C]], dtype=float)
        b = np.array([-D, -E], dtype=float)
        try:
            c = np.linalg.solve(M, b)
        except np.linalg.LinAlgError:
            c, *_ = np.linalg.lstsq(M, b, rcond=None)
        return float(c[0]), float(c[1])

    @staticmethod
    def line_intersections_t(coefficient, c0, v, eps=1e-10):
        A, B, C, D, E, F = map(float, coefficient)
        cx, cy = map(float, c0)
        vx, vy = map(float, v)
        a = A * vx * vx + B * vx * vy + C * vy * vy
        b = 2 * A * cx * vx + B * (cx * vy + cy * vx) + 2 * C * cy * vy + D * vx + E * vy
        c = A * cx * cx + B * cx * cy + C * cy * cy + D * cx + E * cy + F
        if abs(a) < eps:
            if abs(b) < eps:
                return []
            return [-c / b]
        disc = b * b - 4 * a * c
        if disc < -1e-8:
            return []
        disc = max(0.0, disc)
        s = float(np.sqrt(disc))
        return [(-b - s) / (2 * a), (-b + s) / (2 * a)]

    def first_positive_t(self, coefficient, c0, v, eps=1e-9):
        ts = self.line_intersections_t(coefficient, c0, v)
        pos = [t for t in ts if t > eps]
        if not pos:
            raise ValueError("未找到正向交点")
        return min(pos)

