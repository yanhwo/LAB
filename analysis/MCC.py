    def roi_24patches(self):
        """
        调用 segonnx.exe，返回：
          - roi_list: 24 个 ROI，字典含 x_left/x_right/y_top/y_bottom
          - resized_path: seg/resize 之后的图像路径（找不到则为 None）
        """
        # 1) 运行 exe
        subprocess.run([self.seg_tool, "-path", self.path_image],
                       check=True, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)

        # 2) 最新 .txt
        img_dir = os.path.dirname(self.path_image)
        txt_files = [f for f in os.listdir(img_dir) if f.lower().endswith('.txt')]
        if not txt_files:
            raise FileNotFoundError("No TXT file found after running segonnx.exe!")

        txt_files_sorted = sorted(txt_files, key=lambda fn: os.path.getmtime(os.path.join(img_dir, fn)), reverse=True)
        txt_path = os.path.join(img_dir, txt_files_sorted[0])

        # 3) 解析 ROI
        with open(txt_path, "r", encoding="utf-8") as f:
            for line in f:
                parts = line.strip().split()
                if len(parts) == 4:
                    yt, yb, xl, xr = map(int, parts)
                    self.roi.append({"y_top": yt, "y_bottom": yb, "x_left": xl, "x_right": xr})

        if len(self.roi) != 24:
            raise ValueError(f"需要24个ROI，当前={len(self.roi)}")

        # 4) 找到 seg/resize 图（用于后续采样）
        seg_files = [f for f in os.listdir(img_dir)
                     if f.lower().endswith(('.jpg', '.jpeg', '.png')) and 'seg' in f.lower()]
        if seg_files:
            seg_files_sorted = sorted(seg_files, key=lambda fn: os.path.getmtime(os.path.join(img_dir, fn)),
                                      reverse=True)
            roi_image_path = os.path.join(img_dir, seg_files_sorted[0])
        else:
            roi_image_path = None

        roi_image = Image.open(roi_image_path).convert("RGB")
        self.roi_image = np.array(roi_image)
        os.remove(txt_path)
        os.remove(roi_image_path)

    def get_24values(self, method='QCAT'):
        # get roi
        self.roi_24patches()

        rgb_arr = []
        for roi in self.roi:
            x = int(roi["x_left"])
            y = int(roi["y_top"])
            w = int(roi["x_right"] - roi["x_left"])
            h = int(roi["y_bottom"] - roi["y_top"])
            patch = self.roi_image[y:y + h, x:x + w, :]
            mean_rgb = patch.reshape(-1, 3).mean(axis=0)
            rgb_arr.append(mean_rgb)
        rgb_arr = np.array(rgb_arr)

        # 实测 Lab（D65）
        rgb01 = np.clip(rgb_arr/255.0, 0, 1)
        Lab_meas = color.rgb2lab(rgb01.reshape(-1, 1, 3)).reshape(-1, 3)

        # 参考 Lab
        if method == 'Imatest':
            Lab_ref = self.REF_LAB_TABLE_Ima[self.cct]
        else:
            Lab_ref = self.REF_LAB_TABLE[self.cct]

        # 指标
        Lm, am, bm = Lab_meas[:, 0], Lab_meas[:, 1], Lab_meas[:, 2]
        Lr, ar, br = Lab_ref[:, 0], Lab_ref[:, 1], Lab_ref[:, 2]
        Cm = np.hypot(am, bm)
        Cr = np.hypot(ar, br)
        Hm = (np.degrees(np.arctan2(bm, am)) + 360) % 360
        dL = Lm - Lr
        dC = Cm - Cr
        dab = np.sqrt((am-ar)**2 + (bm-br)**2)
        dE76 = color.deltaE_cie76(Lab_meas, Lab_ref)
        dE2000 = colour.delta_E(Lab_meas, Lab_ref, method='cie2000')
        dH = np.sqrt(np.maximum(dE76**2 - dL**2 - dC**2, 0.0))

        self.patch_values = []
        for i in range(24):
            self.patch_values.append({
                "ID": f"{i+1:02d}",
                # "Patch": self.PATCH_NAMES[i],
                "R": float(np.round(rgb_arr[i, 0], 2)),
                "G": float(np.round(rgb_arr[i, 1], 2)),
                "B": float(np.round(rgb_arr[i, 2], 2)),
                "L": float(np.round(Lm[i], 2)),
                "a": float(np.round(am[i], 2)),
                "b": float(np.round(bm[i], 2)),
                "H": float(np.round(Hm[i], 2)),
                "C": float(np.round(Cm[i], 2)),
                "ΔL": float(np.round(dL[i], 2)),
                "ΔC": float(np.round(dC[i], 2)),
                "Δab": float(np.round(dab[i], 2)),
                "ΔH": float(np.round(dH[i], 2)),
                "ΔE76": float(np.round(dE76[i], 2)),
                "ΔE2000": float(np.round(dE2000[i], 2))
            })

    def save_data(self, metric):
        if os.path.exists(self.path_data):
            shutil.rmtree(self.path_data)
        os.makedirs(self.path_data, exist_ok=False)

        with open(self.path_txt, 'w', encoding='utf-8') as f:
            # 1. light condition
            f.write(f'{self.cct} {self.light}\n\n')

            # 2. Patch Values
            headers = list(self.patch_values[0].keys())  # 确保headers是列表（有序）
            columns = []
            for key in headers:
                col_content = [key]
                for item in self.patch_values:
                    col_content.append(str(item[key]))
                columns.append(col_content)

            extra_width = 6
            max_widths = [max(len(content) for content in col) + extra_width for col in columns]

            header_parts = [header.ljust(width) for header, width in zip(headers, max_widths)]
            f.write(''.join(header_parts) + '\n')

            for item in self.patch_values:
                data_parts = [str(item[key]).ljust(width) for key, width in zip(headers, max_widths)]
                f.write(''.join(data_parts) + '\n')
            f.write('\n')

            pil_roi = Image.fromarray(self.roi_image)
            pil_roi.save(self.path_roi)

            # 3.1 AE Data
            if 'Exposure' in metric:
                f.write('[Exposure]\n')
                f.write(f'Value: {self.AE.ae_value}\n')
                f.write(f'Score: {self.AE.ae_score}\n')
                f.write(f'Result: {self.AE.ae_result}\n')
                f.write(f'Criteria: >= {self.AE.ae_criteria}\n')
                f.write(f'\n')
            # 3.2 AWB Data
            if 'White Balance' in metric:
                f.write('[White Balance]\n')
                f.write(f'Value: {self.AWB.awb_values}\n')
                f.write(f'Score: {self.AWB.awb_score}\n')
                f.write(f'Result: {self.AWB.awb_result}\n')
                f.write(f'Criteria: >= {self.AWB.awb_criteria}\n')
                f.write(f'\n')
            # 3.3 Color Data
            if 'Color Fidelity' in metric:
                f.write('[Color Fidelity]\n')
                f.write(f'Value: {self.ColorFidelity.color_values}\n')
                f.write(f'Score: {self.ColorFidelity.color_score}\n')
                f.write(f'Result: {self.ColorFidelity.color_result}\n')
                f.write(f'Criteria: >= {self.ColorFidelity.color_criteria}\n')
                f.write(f'\n')

    def evaluate(self, metric=('Exposure', 'White Balance', 'Color Fidelity')):
        if 'Exposure' in metric:
            self.AE = Exposure(self.patch_values, self.cct, self.light)
            self.AE.eva_ae()
            self.info['score']['Exposure'] = self.AE.ae_score
            self.info['result']['Exposure'] = self.AE.ae_result
            self.info['value']['luma'] = self.AE.ae_value
        if 'White Balance' in metric:
            self.AWB = WhiteBalance(self.patch_values, self.cct, self.light)
            self.AWB.eva_awb()
            self.info['score']['White Balance'] = self.AWB.awb_score
            self.info['result']['White Balance'] = self.AWB.awb_result
            self.info['value'] = self.AWB.awb_values
        if 'Color Fidelity' in metric:
            self.ColorFidelity = ColorFidelity(self.patch_values, self.cct, self.light)
            self.ColorFidelity.eva_color()
            self.info['score']['Color Fidelity'] = self.ColorFidelity.color_score
            self.info['result']['Color Fidelity'] = self.ColorFidelity.color_result
            self.ColorFidelity.eva_color_IOT()
            self.info['score']['Color Fidelity(IOT)'] = self.ColorFidelity.color_score
            self.info['result']['Color Fidelity(IOT)'] = self.ColorFidelity.color_result
        self.save_data(metric)


