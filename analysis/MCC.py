    @staticmethod
    def f_val(coefficient, p):
        A, B, C, D, E, F = coefficient
        x, y = p
        return A * x * x + B * x * y + C * y * y + D * x + E * y + F

    @staticmethod
    def ellipse_center(A, B, C, D, E, _):
        M = np.array([[2 * A, B], [B, 2 * C]], dtype=float)
        k = np.array([D, E], dtype=float)
        return -np.linalg.solve(M, k)

    @staticmethod
    def line_intersections_with_ellipse(coefficient, c0, u):
        A, B, C, D, E, F = coefficient
        x0, y0 = c0
        ux, uy = u
        a = A * ux * ux + B * ux * uy + C * uy * uy
        b = 2 * A * x0 * ux + B * (x0 * uy + y0 * ux) + 2 * C * y0 * uy + D * ux + E * uy
        c = A * x0 * x0 + B * x0 * y0 + C * y0 * y0 + D * x0 + E * y0 + F
        if abs(a) < 1e-14:
            if abs(b) < 1e-14:
                return []
            return [-c / b]
        disc = b * b - 4 * a * c
        if disc < 0:
            disc = 0.0
        s = np.sqrt(disc)
        return [(-b - s) / (2 * a), (-b + s) / (2 * a)]

    def score_point_ab(self, p, green_coefficient, red_coefficient):
        fg = self.f_val(green_coefficient, p)
        fr = self.f_val(red_coefficient, p)
        if fg <= 0:
            return 100.0, "inside_green"
        if fr >= 0:
            return 0.0, "outside_red"

        # 夹层：沿绿心 → 点方向线性插值
        cg = self.ellipse_center(*green_coefficient)
        v = np.array(p, dtype=float) - cg
        n = np.linalg.norm(v)
        if n == 0:
            return 100.0, "inside_green(center)"
        u = v / n

        tg_list = [t for t in self.line_intersections_with_ellipse(green_coefficient, cg, u) if t >= -1e-9]
        tr_list = [t for t in self.line_intersections_with_ellipse(red_coefficient, cg, u) if t >= -1e-9]
        tg = max(tg_list) if tg_list else None
        tr = max(tr_list) if tr_list else None
        tp = n

        if tg is None or tr is None or tr <= tg + 1e-9:
            return 50.0, "between(fallback)"

        score = 100.0 * (1.0 - (tp - tg) / (tr - tg))
        return float(np.clip(score, 0.0, 100.0)), "between"

    def eva_awb(self):
        # 1.0 get patch 20, 21, 22, 23
        neutral_ab = []
        neutral_rg = []
        neutral_bg = []
        for i in self.TEST_PATCH_NO:
            a = float(self.patch_values[i - 1]["a"])
            b = float(self.patch_values[i - 1]["b"])
            R = float(self.patch_values[i - 1]["R"])
            B = float(self.patch_values[i - 1]["B"])
            G = float(self.patch_values[i - 1]["G"])
            neutral_ab.append((a, b))
            neutral_rg.append(R / G)
            neutral_bg.append(B / G)

        green = self.WB_ELLIPSES[self.cct]["green"]
        red = self.WB_ELLIPSES[self.cct]["red"]

        wb_list = []
        for p in neutral_ab:
            s, tag = self.score_point_ab(p, green, red)
            wb_list.append((float(p[0]), float(p[1]), float(round(s, 2)), tag))

        self.awb_score = float(round(np.mean([d[2] for d in wb_list]), 2))
        self.awb_result = "Pass" if self.awb_score >= self.awb_criteria else "Fail"
        self.awb_values = {
            "r/g": neutral_rg,
            "b/g": neutral_bg
        }


